              <h4>How Do You Care For Gold Jewelry?</h4>
              <p class="margin-right">Between everyday wear and contact with undetectable chemicals, gold jewelry requires periodic cleaning.<a href="#" class="twitter-quote-link" target="_blank"><span class="twitter-quote">Regularly checking your jewelry for noticeable scratches or changes to the gold surface will let you know when it is time for a cleaning.<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;To properly clean your jewelry, all that is needed is a soft bristled toothbrush and a bowl with a mixture of light soap and water. Also, a soft cloth to dry your gold is helpful. If you prefer, there are also a number of gold-specific jewelry polishes to choose from that perform nicely. Be careful if you do decide to buy a cleaning polish as not all of them contain ingredients safe for your gold. Broken chains, noticeable dents, and deep scratches require the services of a jeweler or repairman. Although there are many ways to care for your jewelry in the comfort of your home, an occasional check up with your jeweler is recommended to preserve your gold.</p> 
