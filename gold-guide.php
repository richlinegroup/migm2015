              <h4 class="gold-guide-section-titles">What is Gold Alloy?</h4>
              <p class="">As we mentioned earlier, pure gold is always mixed with alloys to create karat gold. Alloys are created when at least two different metals are combined. All karat gold jewelry uses alloys to help enhance the pure gold, but it is important to note that not all alloys are created equal.
                Alloys provide hardness and durability, as well as color variations in gold, depending on the type of alloy metals added.
                Alloys can impact other factors such as weight, strength, shine, and in certain cases, factors such as the hypoallergenic nature of gold. For example, titanium alloy combined with gold is a strong source material for designers. <a href="#" class="twitter-quote-link"><span class="twitter-quote" data-quote="Know what other metals are in your gold jewelry!">It is important to inquire into the nature of the alloy used in a gold product to understand how it enhances the character of your gold jewelry.&nbsp;<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;
