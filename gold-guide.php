              <h4>What is Karat Gold?</h4>
              <p class="margin-right">Much of what people think of when they think of gold jewelry is known as Karat Gold. Karat Gold mixes pure gold, which is usually too soft for everyday wear, with what are known as alloys. Karatage is what tells you the purity of your gold jewelry.<a href="#" class="twitter-quote-link" target="_blank"><span class="twitter-quote">Karat Gold is always measured in 24 parts. The 24 part measurement defines the ratio between pure gold and the alloy.<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;For example, in the case of 18 Karat Gold, 18 out of 24 parts Pure Gold. The remaining six parts are the alloy. The karatage of gold is always displayed as a casting mark on your jewelry. It can be shown in one of two ways. In the case of 18 Karat Gold, it will either be displayed as 18K or as a ratio of 1000 parts. For example, a 750 casting mark means a gold product is 75% Pure Gold or 18 Karat.</p> 
