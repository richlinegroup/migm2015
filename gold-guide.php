              <h4 class="gold-guide-section-titles">What is Karat Gold?</h4>
              <p>
              
              <p class="">Gold in its purest state is referred to as 24-karat gold. This level of gold is rarely used in gold jewelry because it is too soft. Gold jewelry is usually alloyed or combined with other metals for increased strength and workability, a practice that is thousands of years old.
                Karatage tells you the purity of your gold jewelry. Much of what people think of when they think of gold jewelry is known as karat gold.
                Most American gold jewelry is made of 14-karat gold. It is marked with either 14k or 585. The 14k marking means it is made of 14 parts gold combined or alloyed with 10 parts of other metals to add strength and color. The 585 marking means it is 58.0% to 58.5% pure gold.
                Eighteen-karat gold, marked 18k or 750, is 18 parts gold and 6 parts other metals and is 75% pure gold. Ten-karat gold, marked 10k or 416, is 10 parts gold and 14 parts other metals, and is only 41% pure gold. <a href="#" class="twitter-quote-link"><span class="twitter-quote" data-quote="Gold jewelry must be 10k or more in US.">Nothing less than 10k gold can legally be sold as gold jewelry in the United States.&nbsp;<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;
                Here are the most commonly used varieties of karat gold. Please note that the United States requires a minimum of 10 karats to be used in jewelry (although 9-karat jewelry is very popular in the United Kingdom).
               </p><br> 
