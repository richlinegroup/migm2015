              <h4>What is Gold Alloy?</h4>
              <p class="margin-right">As we mentioned earlier, Pure Gold is always mixed with alloys to create Karat Gold. Alloys are created when at least two different metals are combined. All Karat Gold jewelry uses alloys to help enhance the pure gold, but it is important to note that not all alloys are created equal. Alloys can impact factors such as weight, color, strength, shine, and in certain cases factors such as the hypoallergenic nature of gold. For example, titanium alloy combined with gold is a strong source material for designers.<a href="#" class="twitter-quote-link" target="_blank"><span class="twitter-quote">It is important to inquire into the nature of the alloy used in a gold product to understand how it enhances the character of your gold jewelry.<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;</p> 
