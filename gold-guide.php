              <h4 class="gold-guide-section-titles">How Do You Care For Gold Jewelry?</h4>
              <p class="">Between everyday wear and contact with undetectable chemicals, gold jewelry requires periodic cleaning. Regularly checking your gold jewelry for noticeable scratches or changes to the gold surface will let you know when it is time for a cleaning. To properly clean your jewelry, use a soft bristled toothbrush and a bowl with a mixture of light soap and water. Dry your gold with a soft cloth.
                If you prefer, there are a number of gold-specific jewelry polishes to choose from that perform nicely. <a href="#" class="twitter-quote-link"><span class="twitter-quote" data-quote="Not all gold cleaning products are safe!">Be careful if you decide to buy a cleaning polish, as not all of them contain ingredients safe for your gold.&nbsp;<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;Broken chains, noticeable dents, and deep scratches require the services of a jeweler or repairman. Although there are many ways to care for your gold jewelry in the comfort of your home, an occasional check-up with your jeweler is recommended to preserve your gold.
