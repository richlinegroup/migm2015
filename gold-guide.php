              <h4 class="gold-guide-section-titles">What Are Some Karat Gold Alternatives?</h4>

              <p class="">Part of what makes karat gold so appealing is the fact that pure gold and gold alloy are combined to create a metal that has gold throughout. From the surface to the center, the amount of pure gold throughout your jewelry is consistent.
                <a href="#" class="twitter-quote-link"><span class="twitter-quote" data-quote="There are many alternatives to karat gold.">On the other hand, techniques like vermeil, plated gold, gold-filled, and bonded gold offer cost-conscious alternatives to karat gold.&nbsp;<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;Unlike karat gold, these alternatives are not gold throughout.
                The techniques vary, but they all begin with a core of base metal (such as brass, nickel or steel) or silver (in the case of vermeil and bonded) and add thin layers of gold over the top of the base. The total amount of gold used can vary, but gold-filled traditionally offers the greatest gold thickness, with a minimum of 10% of the total weight being gold.
                It is important to keep longevity and care in mind when purchasing vermeil or plated gold jewelry. Over time the gold can be worn or polished away, allowing the base metal to show through. However, all varieties can be re-plated if needed. Because of the thicker covering on gold-filled and bonded gold jewelry, the external gold should last for years. Thinly plated gold is typically used for costume/fashion jewelry, while gold-filled and vermeil are commonly used as fine jewelry alternatives.
              </p><br> 

