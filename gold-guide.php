              <p class="">The various techniques used to transform the surface on your gold jewelry are known as finishes. Possible gold jewelry finishes range from bright polish, to matte sandblasting, to hammered textures. The finish on your gold jewelry significantly impacts its final look and style.
                Bright polish allows the true nature of gold to shine through, while an array of brushed, satin, matte, engraved and hammered finishes allow designers to expand upon on what is traditionally expected from gold jewelry.
                <a href="#" class="twitter-quote-link"><span class="twitter-quote" data-quote="Gold can have many different finishes applied.">Designers often combine multiple complementary, or even contrasting, finishes to create new and exciting gold jewelry styles.&nbsp;<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;Jewelry craftsmen continually experiment with new hand and machine techniques to push the boundaries of gold finishes and their impact on the latest jewelry designs.
