              <h4>How Is Gold Jewelry Priced?</h4>
              <p class="margin-right">Several factors play into the cost of your gold jewelry. Beyond the inherent price of gold at any given time, karatage – the amount of pure gold vs. less expensive alloys – is one of the key factors behind cost. The higher the karatage, the more expensive the item will be due to the amount of pure gold that it contains.</p> 

              <p class="margin-right">The weight of the gold in jewelry is also a significant indicator of price.<a href="#" class="twitter-quote-link" target="_blank"><span class="twitter-quote">While there is often a desire to simply purchase lighter gold jewelry to cut down on costs, it is essential to consider how it will be worn.<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;A substantial amount of gold is recommended for everyday pieces (such as a wedding band) or pieces with a higher possibility for abrupt contact (such as bangles). On the other hand, lighter pieces work well when dealing with earrings or pendants, where the risk of contact is lower and comfort trumps durability.</p> 

              <p class="margin-right">The process used to create your jewelry also plays a role in its cost. The creation of gold jewelry usually starts with one of two processes, casting or stamping. Castings are made through the use of a mold and molten Karat Gold. Stampings are created through the use of a press. Custom and more substantial jewelry pieces tend to be casted, while stamping is used for crafting commonly used parts, such as earring backings. The majority of jewelry sold in stores is created using a combination of casted metal and stamped metal components, which are then finished by jewelry artisans. The extent to which artisans are needed to craft the final piece can also play a significant role in the final price. For example, a straightforward assembly would require less time and incur less added cost than substantial hand engraving.</p>

              <p class="margin-right">Last, but not least, it's important to consider any other materials used in the creation of a design as gold is often combined with an array of precious and semi-precious stones, as well as diamonds that must be factored into the final price.</p>
