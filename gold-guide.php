                Gold is, and always has been, recycled. Pure gold is indestructible; it will not corrode, rust or tarnish and cannot be destroyed by fire. All the gold taken from the earth during all of recorded history is still being melted and re-melted and used again and again.
                With a rich history that dates back over 6,000 years, gold jewelry continues to evoke feelings of value and desire. <a href="#" class="twitter-quote-link"><span class="twitter-quote" data-quote="You could be wearing the gold of queens and kings!">The gold you own and wear today may once have adorned King Solomon’s Temple or been worn by Cleopatra.&nbsp;<span><img src="img/twitter-icon-white.png"/></span></span></a>&nbsp;
