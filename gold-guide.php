              <h4 class="gold-guide-section-titles">Why Choose Gold Jewelry?</h4>            
              <p class="">
                What do you really know about your gold jewelry? Have you thought about where it comes from or why you like the look and feel of gold?
                Gold jewelry is always in high demand, but few understand why it is valuable. Creating beautiful gold chains, bangles, necklaces or earrings is a balance of art and science. In this gold jewelry buying guide we aim to help you understand what goes into creating the perfect piece of gold jewelry.
                Gold combines the four characteristics of lustrous beauty, virtual indestructibility, extreme rarity, and ease of workability, making it the ideal metal for jewelry.
                Being the most malleable of all metals, a one-ounce piece of gold can be worked down to 5 micrometers thick – that is 1/10 the diameter of a human hair – and laid out into 50 miles of wire.
